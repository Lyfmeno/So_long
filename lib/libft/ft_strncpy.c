#include "libft.h"

char	*ft_strncpy(char *dst, const char *src, size_t n)
{
	size_t	i;

	i = 0;
	while (src[i] && i < n && n > 0)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
